def categorize_keyword(keyword: str) -> str:
    """根据关键字推断其分类"""
    for category, keywords in KEYWORD_CATEGORIES.items():
        if keyword in keywords:
            return category
    return "其他"


def get_keyword_doc(keyword: str, lang_name: str = "") -> str | None:
    """获取关键字的 hover 文档（Markdown 格式）

    Args:
        keyword: 关键字
        lang_name: 语言中文名（可选）

    Returns:
        Markdown 格式文档，或 None
    """
    category = categorize_keyword(keyword)
    if category == "其他":
        return None

    lines = [f"**{keyword}**"]
    if lang_name:
        lines[0] += f" — {lang_name}"

    lines.append("")
    lines.append(f"分类: {category}")
    desc = CATEGORY_DESCRIPTIONS.get(category, "")
    if desc:
        lines.append(f"说明: {desc}")

    example = KEYWORD_EXAMPLES.get(keyword)
    if example:
        lines.append("")
        lines.append(f"示例: `{example}`")

    return "\n".join(lines)
